    private void jLabel4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel4MouseClicked
        MRU mru = new MRU();
        try {
            double tiempo = Double.parseDouble(jTextField1.getText());
            double distancia = Double.parseDouble(jTextField2.getText());
            mru.setDistancia(distancia);
            mru.setTiempo(tiempo);
            String resultado = String.valueOf(mru.obtenerVelocidadX(tiempo, distancia));
            jLabel5.setText(resultado + " m/s");
            jLabel5.setVisible(true);
            mru.setVelocidad(Double.parseDouble(resultado));

            if (ma.deserializarMRU(jlNombreUsuario.getText(), "MRU", "Velocidad") != null) {
                mrut = ma.deserializarMRU(jlNombreUsuario.getText(), "MRU", "Velocidad");
                mrut.add(mru);
            } else if (ma.deserializarMRU(jlNombreUsuario.getText(), "MRU", "Velocidad") == null) {
                mrut.add(mru);
                ma.serializarMRU(mrut, jlNombreUsuario.getText(), "MRU", "Velocidad");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Solo puedes ingresar numeros y un punto", "Formato Incorrecto", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jLabel4MouseClicked

